def tokenise(json_obj: Dict[str, Any]) -> List[str]:
    """
    Very lightweight tokeniser that respects:
      - strings → quoted tokens
      - numbers → bare tokens
      - dicts/lists → brackets tokens
    """
    s = json.dumps(json_obj, separators=(',', ':'), ensure_ascii=False)
    # simple regex tokenisation
    tokens = re.findall(r'"[^"]*"|[^,{}:"]+', s)
